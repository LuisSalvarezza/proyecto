    private void btnAgregarAspiranteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAspiranteActionPerformed
       
                // Obtenemos el primer dato del renglon seleccionado
        if (jTableEscuelas.getSelectedRow() != -1) 
        {
              int filaseleccionada = jTableEscuelas.getSelectedRow();
              String nombreEscuela = (String)jTableEscuelas.getValueAt(filaseleccionada, 0);
      
              Escuela escuela = _controladorFrm.ObtenerEscuelaPorNombre(nombreEscuela);
              
              if(escuela!=null )
              {
                    Aspirante entidad = new Aspirante();
                    
                    frmCrubAspirante formulario = new frmCrubAspirante(entidad);
                    formulario.setControlador(_controladorFrm);
                    formulario.setTipoOperacion(1);
                    formulario.setEscuela(escuela);
                    formulario.setFormulario(this);
                    formulario.setVisible(true);
              }

              
              
        }
        else
        {
         JOptionPane.showMessageDialog(null, "Para agregar un aspirante hay que seleccionar una escuela", 
        "Aviso", JOptionPane.OK_OPTION); 
        
        }
        
        
        
        
        
        
        

        
    }//GEN-LAST:event_btnAgregarAspiranteActionPerformed

